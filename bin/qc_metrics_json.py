def _stat_metrics(prefix, subject, unit=None):
    return [
        {
            "name": f"{prefix}_{stat}",
            "description": f"{STAT_DESCRIPTIONS[stat]} {subject}",
            "unit": unit,
        }
        for stat in ["median", "mean", "std", "min", "max", "q25", "q75"]
    ]


