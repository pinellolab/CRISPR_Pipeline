def _json_safe(value):
    if isinstance(value, dict):
        return {str(k): _json_safe(v) for k, v in value.items()}
    if isinstance(value, (list, tuple)):
        return [_json_safe(v) for v in value]
    if isinstance(value, pd.Series):
        return _json_safe(value.to_dict())
    if isinstance(value, np.ndarray):
        return _json_safe(value.tolist())
    if isinstance(value, (np.integer,)):
        return int(value)
    if isinstance(value, (np.floating,)):
        value = float(value)
    if isinstance(value, float):
        return None if math.isnan(value) or math.isinf(value) else value
    if not isinstance(value, (str, bytes, bool)):
        try:
            if pd.isna(value):
                return None
        except Exception:
            pass
    return value


def _table_records(path):
    if not path or not os.path.exists(path):
        return []
    try:
        return pd.read_csv(path, sep="\t").to_dict(orient="records")
    except Exception:
        return []


def _load_json(path):
    try:
        with open(path, "r") as handle:
            return json.load(handle)
    except Exception:
        return {}


def collect_mapping_json(json_dir):
    if not json_dir or not os.path.isdir(json_dir):
        return []
    groups = {}
    for file_name in sorted(os.listdir(json_dir)):
        if not file_name.endswith(".json") or "-" not in file_name:
            continue
        stem = file_name[:-5]
        if stem.endswith("-inspect"):
            key = stem[:-8]
            groups.setdefault(key, {})["inspect"] = os.path.join(json_dir, file_name)
        elif stem.endswith("-run_info"):
            key = stem[:-9]
            groups.setdefault(key, {})["run_info"] = os.path.join(json_dir, file_name)

    records = []
    for key, paths in sorted(groups.items()):
        if "-" in key:
            modality, measurement_set = key.split("-", 1)
        else:
            modality, measurement_set = key, key
        inspect = _load_json(paths.get("inspect"))
        run_info = _load_json(paths.get("run_info"))
        metrics = {}
        metrics.update(run_info)
        metrics.update(inspect)
        records.append(
            {
                "modality": modality,
                "measurement_set": measurement_set,
                "source_files": {
                    "inspect": os.path.basename(paths["inspect"]) if "inspect" in paths else None,
                    "run_info": os.path.basename(paths["run_info"]) if "run_info" in paths else None,
                },
                "metrics": _json_safe(metrics),
            }
        )
    return records


def collect_additional_qc(additional_qc_dir):
    specs = {
        "gene": ("additional_qc_gene", "gene/gene_metrics.tsv"),
        "guide": ("additional_qc_guide", "guide/guide_metrics.tsv"),
        "intended_target": ("additional_qc_intended_target", "intended_target/intended_target_metrics.tsv"),
        "trans": ("additional_qc_trans", "trans/trans_metrics.tsv"),
    }
    results = {}
    for key, (catalog_key, rel_path) in specs.items():
        path = os.path.join(additional_qc_dir, rel_path) if additional_qc_dir else None
        results[key] = {
            "catalog_key": catalog_key,
            "source_artifact": rel_path,
            "available": bool(path and os.path.exists(path)),
            "rows": _json_safe(_table_records(path)),
        }
    return results


def _count_matrix_rows(matrix, threshold=0):
    if hasattr(matrix, "getnnz") and threshold == 0:
        values = matrix.getnnz(axis=1)
    else:
        values = (matrix > threshold).sum(axis=1)
    return np.asarray(values).ravel()


def _obs_less_than(adata, column, threshold):
    if column not in adata.obs.columns:
        return None
    values = pd.to_numeric(adata.obs[column], errors="coerce")
    return int((values < threshold).sum())


def _matrix_sum_vector(matrix, axis):
    return np.asarray(matrix.sum(axis=axis)).ravel()


def _matrix_mean_sum(matrix, axis):
    values = _matrix_sum_vector(matrix, axis=axis)
    return float(values.mean()) if values.size else None


def _guide_assignment_summary(guide_mod):
    assignment = None
    if hasattr(guide_mod, "layers"):
        try:
            assignment = guide_mod.layers.get("guide_assignment")
        except Exception:
            assignment = None
    if assignment is None:
        return {
            "total_sgrna_assignment_values": None,
            "median_sgrna_assignment_per_guide": None,
        }
    values_per_guide = _matrix_sum_vector(assignment, axis=0)
    return {
        "total_sgrna_assignment_values": float(values_per_guide.sum()) if values_per_guide.size else 0.0,
        "median_sgrna_assignment_per_guide": float(np.median(values_per_guide)) if values_per_guide.size else None,
    }


def _selected_params(params):
    keys = [
        "QC_barcode_filter",
        "QC_min_genes_per_cell",
        "QC_min_cells_per_gene",
        "QC_pct_mito",
        "QC_batch_col",
        "ENABLE_SCRUBLET",
        "ENABLE_DATA_HASHING",
        "GUIDE_ASSIGNMENT_method",
        "GUIDE_ASSIGNMENT_capture_method",
        "INFERENCE_method",
        "INFERENCE_max_target_distance_bp",
        "Multiplicity_of_infection",
    ]
    return {key: params.get(key) for key in keys if key in params}


def build_pipeline_qc_metrics_payload(
    *,
    params,
    qc_params,
    sample_counts,
    concat_count,
    filter_count,
    filter_info,
    filtered_count,
    guide_intersection_count,
    final_count,
    mudata,
    gene_ann,
    gene_filtered_ann,
    guide_ann,
    json_dir=None,
    additional_qc_dir=None,
    hashing_counts=None,
    use_default=False,
):
    params = params or {}
    gene_mod = mudata.mod["gene"]
    guide_mod = mudata.mod["guide"]
    detected_genes = _count_matrix_rows(gene_mod.X, threshold=0)
    no_gene_count_gt_1 = int((_count_matrix_rows(gene_mod.X, threshold=1) < 1).sum())
    unfiltered_intersection = len(set(gene_ann.obs_names).intersection(guide_ann.obs_names))
    guide_assignment = _guide_assignment_summary(guide_mod)
    sample_count_records = [
        {"measurement_set": str(name), "unfiltered_barcodes": int(count)}
        for name, count in (sample_counts or [])
    ]
    raw_total = sum(item["unfiltered_barcodes"] for item in sample_count_records) if sample_count_records else None

    filtering_summary = {
        "raw_scRNA_barcodes_per_measurement_set": sample_count_records,
        "raw_scRNA_barcodes_total": raw_total,
        "concatenated_scRNA_cells": int(concat_count),
        "barcode_filter": {
            "method": qc_params.get("barcode_filter"),
            "params_source": qc_params.get("barcode_filter_source"),
            "total_gene_umis_min": None if not filter_info else filter_info.get("threshold"),
            "knee_rank": None if not filter_info else filter_info.get("knee_rank"),
            "cells_after_filter": int(filter_count) if filter_count is not None else None,
            "min_genes_per_cell": qc_params.get("min_genes"),
            "min_genes_rule_active": qc_params.get("barcode_filter") not in {"knee", "knee2"},
        },
        "mitochondrial_filter": {
            "pct_mito_max": qc_params.get("pct_mito"),
            "params_source": qc_params.get("pct_mito_source"),
            "cells_after_filter": int(filtered_count) if filtered_count is not None else None,
        },
        "modality_intersection": {
            "rna_guide_intersection_cells": int(guide_intersection_count),
            "guide_unfiltered_intersection_cells": int(unfiltered_intersection),
            "guide_unfiltered_intersection_fraction": (
                unfiltered_intersection / int(guide_ann.n_obs) if int(guide_ann.n_obs) > 0 else None
            ),
            "guide_cells": int(guide_ann.n_obs),
            "guide_features": int(guide_ann.n_vars),
        },
        "final_mudata": {
            "cells": int(final_count),
            "gene_cells": int(gene_mod.n_obs),
            "gene_features": int(gene_mod.n_vars),
            "guide_cells": int(guide_mod.n_obs),
            "guide_features": int(guide_mod.n_vars),
            "gene_mean_umis_per_cell": _matrix_mean_sum(gene_mod.X, axis=1),
            "mean_guides_per_cell": _matrix_mean_sum(guide_mod.X, axis=1),
            "mean_cells_per_guide": _matrix_mean_sum(guide_mod.X, axis=0),
            **guide_assignment,
        },
        "diagnostics": {
            "final_cells_total_gene_umis_lt_100": _obs_less_than(gene_mod, "total_gene_umis", 100),
            "final_cells_total_gene_umis_lt_500": _obs_less_than(gene_mod, "total_gene_umis", 500),
            "final_cells_no_gene_count_gt_1": no_gene_count_gt_1,
            "median_detected_genes_final_cells": float(np.median(detected_genes)) if detected_genes.size else None,
        },
    }

    if hashing_counts:
        filtering_summary["hashing"] = _json_safe(hashing_counts)

    return _json_safe(
        {
            "schema_version": "1.0",
            "description": (
                "CRISPR Pipeline QC metric catalog and observed run-level QC values. "
                "The catalog describes metrics used in pipeline additional_qc outputs and dashboard QC cards."
            ),
            "dashboard_default_mode": bool(use_default),
            "parameters": {
                "resolved_params_loaded": bool(qc_params.get("params_found")),
                "selected_qc_and_inference_params": _selected_params(params),
            },
            "metric_catalog": METRIC_CATALOG,
            "dashboard_summary": {
                "filtering_summary": filtering_summary,
            },
            "observed_metrics": {
                "mapping_json": collect_mapping_json(json_dir),
                "additional_qc": collect_additional_qc(additional_qc_dir),
            },
            "sources": {
                "dashboard_builder": "create_dashboard_df_HASHING.py" if hashing_counts else "create_dashboard_df.py",
                "mapping_json_dir": json_dir,
                "additional_qc_dir": additional_qc_dir,
                "gene_ann_cells": int(gene_ann.n_obs),
                "gene_filtered_ann_cells": int(gene_filtered_ann.n_obs),
                "guide_ann_cells": int(guide_ann.n_obs),
            },
        }
    )


def write_pipeline_qc_metrics_json(output_path, **kwargs):
    payload = build_pipeline_qc_metrics_payload(**kwargs)
    with open(output_path, "w") as handle:
        json.dump(payload, handle, indent=2, sort_keys=True)
        handle.write("\n")
    return payload
